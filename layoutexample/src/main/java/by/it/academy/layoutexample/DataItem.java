package by.it.academy.layoutexample;

public class DataItem {

    private int imageId;
    private String title;

    public DataItem(int imageId, String title) {
        this.imageId = imageId;
        this.title = title;
    }

    public int getImageId() {
        return imageId;
    }

    public String getTitle() {
        return title;
    }
}
